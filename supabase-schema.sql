create or replace function public.apply_operation_event()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.type::text = 'partial' then
    update public.operations
      set status = 'partial',
          updated_at = now()
      where id = new.operation_id
        and status in ('open', 'partial');
  elsif new.type::text in ('closed', 'stopped') then
    update public.operations
      set status = 'closed',
          close_price = new.price,
          result_points = (
            select coalesce(sum(e.points), 0)
            from public.operation_events as e
            where e.operation_id = new.operation_id
              and e.type::text in ('partial', 'closed', 'stopped')
          ),
          closed_at = coalesce(closed_at, now()),
          updated_at = now()
      where id = new.operation_id;
  elsif new.type::text = 'canceled' then
    update public.operations
      set status = 'canceled',
          closed_at = coalesce(closed_at, now()),
          updated_at = now()
      where id = new.operation_id;
  end if;

  return new;
end;
$$;

drop trigger if exists apply_operation_event_trigger on public.operation_events;
create trigger apply_operation_event_trigger
  after insert on public.operation_events
  for each row execute function public.apply_operation_event();

create or replace function public.enqueue_operation_message()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  operation_pair text;
  operation_side public.operation_side;
  operation_number bigint;
begin
  if coalesce(new.message_text, '') = '' then
    return new;
  end if;

  select o.pair, o.side, o.operation_number
    into operation_pair, operation_side, operation_number
  from public.operations as o
  where o.id = new.operation_id;

  insert into public.message_queue (operation_event_id, payload)
  values (
    new.id,
    jsonb_build_object(
      'text', new.message_text,
      'operation_id', new.operation_id,
      'event_id', new.id,
      'event_type', new.type,
      'pair', operation_pair,
      'side', operation_side,
      'operation_number', operation_number,
      'price', new.price,
      'points', new.points,
      'close_percent', new.close_percent
    )
  );

  return new;
end;
$$;

drop trigger if exists enqueue_operation_message_trigger on public.operation_events;
create trigger enqueue_operation_message_trigger
  after insert on public.operation_events
  for each row execute function public.enqueue_operation_message();

create or replace view public.monthly_operation_stats as
select
  date_trunc('month', closed_at)::date as month,
  count(*)::integer as closed_operations,
  coalesce(sum(result_points), 0)::numeric as total_points,
  count(*) filter (where result_points > 0)::integer as wins,
  count(*) filter (where result_points < 0)::integer as losses,
  count(*) filter (where result_points = 0)::integer as breakevens,
  coalesce(avg(result_points), 0)::numeric as average_points
from public.operations
where status = 'closed'
  and closed_at is not null
group by 1;

