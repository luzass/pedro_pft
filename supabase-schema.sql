do $$
begin
  if not exists (select 1 from pg_type where typname = 'operation_side') then
    create type public.operation_side as enum ('buy', 'sell');
  end if;

  if not exists (select 1 from pg_type where typname = 'operation_status') then
    create type public.operation_status as enum ('open', 'partial', 'closed', 'canceled');
  end if;

  if not exists (select 1 from pg_type where typname = 'operation_event_type') then
    create type public.operation_event_type as enum ('opened', 'partial', 'closed', 'stopped', 'canceled', 'note');
  end if;
end $$;

