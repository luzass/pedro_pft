create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  role text not null default 'operator' check (role in ('admin', 'operator')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.operations (
  id uuid primary key default gen_random_uuid(),
  pair text not null,
  side public.operation_side not null,
  entry_price numeric not null check (entry_price > 0),
  stop_loss numeric not null check (stop_loss > 0),
  take_profit numeric not null check (take_profit > 0),
  point_size numeric not null default 0.0001 check (point_size > 0),
  status public.operation_status not null default 'open',
  stop_points numeric not null default 0,
  take_profit_points numeric not null default 0,
  close_price numeric,
  result_points numeric,
  notes text,
  created_by uuid references auth.users(id) default auth.uid(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  closed_at timestamptz
);

create table if not exists public.operation_events (
  id uuid primary key default gen_random_uuid(),
  operation_id uuid not null references public.operations(id) on delete cascade,
  type public.operation_event_type not null,
  price numeric,
  points numeric,
  close_percent numeric check (close_percent > 0 and close_percent <= 100),
  note text,
  message_text text,
  message_sent boolean not null default false,
  external_message_id text,
  created_by uuid references auth.users(id) default auth.uid(),
  created_at timestamptz not null default now()
);

create table if not exists public.message_queue (
  id uuid primary key default gen_random_uuid(),
  operation_event_id uuid not null references public.operation_events(id) on delete cascade,
  channel text not null default 'trading_group',
  payload jsonb not null,
  status text not null default 'queued' check (status in ('queued', 'sent', 'failed')),
  attempts integer not null default 0,
  last_error text,
  sent_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.operations
  add column if not exists operation_number bigint generated by default as identity;

with seed as (
  select coalesce(max(operation_number), 0) as value
  from public.operations
),
numbered as (
  select
    o.id,
    (select value from seed) + row_number() over (order by o.created_at, o.id) as operation_number
  from public.operations as o
  where o.operation_number is null
)
update public.operations as o
set operation_number = numbered.operation_number
from numbered
where o.id = numbered.id;

do $$
declare
  operation_number_sequence text;
begin
  select pg_get_serial_sequence('public.operations', 'operation_number')
    into operation_number_sequence;

  if operation_number_sequence is not null then
    perform setval(
      operation_number_sequence,
      coalesce((select max(operation_number) from public.operations), 0) + 1,
      false
    );
  end if;
end $$;

