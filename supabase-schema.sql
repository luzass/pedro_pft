create or replace function public.set_operation_points()
returns trigger
language plpgsql
as $$
begin
  if new.point_size <= 0 then
    raise exception 'point_size must be greater than zero';
  end if;

  if new.side = 'buy' then
    new.stop_points := round(((new.entry_price - new.stop_loss) / new.point_size)::numeric, 1);
    new.take_profit_points := round(((new.take_profit - new.entry_price) / new.point_size)::numeric, 1);
  else
    new.stop_points := round(((new.stop_loss - new.entry_price) / new.point_size)::numeric, 1);
    new.take_profit_points := round(((new.entry_price - new.take_profit) / new.point_size)::numeric, 1);
  end if;

  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists set_operation_points_trigger on public.operations;
create trigger set_operation_points_trigger
  before insert or update of side, entry_price, stop_loss, take_profit, point_size
  on public.operations
  for each row execute function public.set_operation_points();

